package im.point.dotty.network;

public final class Author {
    String login;
    long id;
    String avatar;
    String name;

    public String getLogin() {
        return login;
    }

    public long getId() {
        return id;
    }

    public String getAvatar() {
        return avatar;
    }

    public String getName() {
        return name;
    }
}
